package patternandstring;

public class Stars {
	
	/**
	 * 
	 * 000000
111111
222222
333333   if we print the i the output will come like this
444444
555555

	 * 
	 * 
	 * 012345
012345
012345
012345  if we print the j the output will come like this
012345
012345

	 * 
	 */

	public static void main(String[] args) {
		
		
		for(int i=0;i<=5;i++) {
			
			for(int j=0;j<=5;j++) {
				System.out.print(j);
			}
			System.out.println();
		}
		
		
		
		
		
		
	}
	
	
}
